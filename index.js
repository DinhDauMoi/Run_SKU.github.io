const questionInput = document.getElementById("questionInput"); //ô nhập câu hỏi
const suggestion = document.getElementById("suggestion"); // câu trả lời
const answer = document.getElementById("answer"); // câu hỏi

questionInput.addEventListener("input", function () {
  const inputText = questionInput.value; //lấy value input
  suggestion.innerHTML = "";

  if (inputText.length === 0) {
    answer.innerHTML = "";
    return;
  }

  const matchingQuestions = data.filter(
    (item) => item.question.toLowerCase().includes(inputText.toLowerCase()) //cho về chữ thường
  );

  if (matchingQuestions.length > 0) {
    matchingQuestions.forEach((item) => {
      const question = document.createElement("div");
      question.textContent = item.question;
      suggestion.appendChild(question);
    });
    suggestion.style.display = "block";
  } else {
    suggestion.style.display = "none";
  }
});

suggestion.onclick = (event) => {
  if (event.target.tagName === "DIV") {
    const selectedQuestion = event.target.textContent;
    selectedQuestion.style.fontSize = "100px";
    const selectedAnswer = data.find(
      (item) => item.question === selectedQuestion
    ).answer;
    const formattedAnswer = selectedAnswer.replace(/\$/g, "<br>");
    formattedAnswer.style.fontSize = "100px";
    questionInput.value = selectedQuestion;
    answer.innerHTML = formattedAnswer;
    suggestion.style.display = "none";
  }
};

